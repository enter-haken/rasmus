-module(db_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    lager:start(),
    lager:info("db app started."),
  
    db_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    lager:info("db app stopped."),
    ok.

